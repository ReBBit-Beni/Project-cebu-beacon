let currentLang = 'ja';
const langBtn = document.getElementById('lang-toggle');

if(langBtn) {
    langBtn.addEventListener('click', () => {
        currentLang = currentLang === 'ja' ? 'en' : 'ja';
        langBtn.textContent = currentLang === 'ja' ? 'EN' : 'JA';
        updateContent();
    });
}

function updateContent() {
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[currentLang][key]) {
            if (el.tagName === 'TITLE') {
                document.title = translations[currentLang][key];
            } else {
                el.innerHTML = translations[currentLang][key];
            }
        }
    });
    document.documentElement.lang = currentLang;
}

// Map Particle Animation for Hero Background
const canvas = document.getElementById('particles');
const ctx = canvas.getContext('2d');

let particles = [];
let routes = [];

function resizeCanvas() {
    canvas.width = window.innerWidth;
    canvas.height = window.innerHeight;
}

window.addEventListener('resize', resizeCanvas);
resizeCanvas();

class Particle {
    constructor() {
        this.reset();
    }

    reset() {
        this.x = Math.random() * canvas.width;
        this.y = Math.random() * canvas.height;
        this.size = Math.random() * 2 + 1;
        this.speedX = (Math.random() - 0.5) * 0.5;
        this.speedY = (Math.random() - 0.5) * 0.5;
        this.life = Math.random() * 100 + 50;
        this.maxLife = this.life;
        this.opacity = Math.random() * 0.5 + 0.1;
    }

    update() {
        this.x += this.speedX;
        this.y += this.speedY;
        this.life--;

        if (this.life <= 0 || this.x < 0 || this.x > canvas.width || this.y < 0 || this.y > canvas.height) {
            this.reset();
            // Start from edges sometimes to simulate incoming traffic
            if (Math.random() > 0.8) {
                if (Math.random() > 0.5) {
                    this.x = Math.random() > 0.5 ? 0 : canvas.width;
                    this.y = Math.random() * canvas.height;
                } else {
                    this.y = Math.random() > 0.5 ? 0 : canvas.height;
                    this.x = Math.random() * canvas.width;
                }
            }
        }
    }

    draw() {
        const currentOpacity = this.opacity * (this.life / this.maxLife);
        ctx.fillStyle = `rgba(0, 240, 255, ${currentOpacity})`;
        ctx.beginPath();
        ctx.arc(this.x, this.y, this.size, 0, Math.PI * 2);
        ctx.fill();
    }
}

// Generate some data routes (simulated map connections)
class Route {
    constructor() {
        this.startX = Math.random() * canvas.width;
        this.startY = Math.random() * canvas.height;
        this.endX = this.startX + (Math.random() - 0.5) * 300;
        this.endY = this.startY + (Math.random() - 0.5) * 300;
        this.progress = 0;
        this.speed = Math.random() * 0.01 + 0.002;
    }

    update() {
        this.progress += this.speed;
        if (this.progress >= 1) {
            this.progress = 0;
            this.startX = this.endX;
            this.startY = this.endY;
            this.endX = this.startX + (Math.random() - 0.5) * 300;
            this.endY = this.startY + (Math.random() - 0.5) * 300;
            
            // Keep within bounds
            if (this.endX < 0 || this.endX > canvas.width || this.endY < 0 || this.endY > canvas.height) {
                this.startX = canvas.width / 2;
                this.startY = canvas.height / 2;
            }
        }
    }

    draw() {
        const curX = this.startX + (this.endX - this.startX) * this.progress;
        const curY = this.startY + (this.endY - this.startY) * this.progress;

        ctx.strokeStyle = `rgba(59, 130, 246, 0.15)`;
        ctx.lineWidth = 1;
        ctx.beginPath();
        ctx.moveTo(this.startX, this.startY);
        ctx.lineTo(this.endX, this.endY);
        ctx.stroke();

        ctx.fillStyle = `rgba(0, 240, 255, ${Math.sin(this.progress * Math.PI)})`;
        ctx.shadowBlur = 10;
        ctx.shadowColor = '#00f0ff';
        ctx.beginPath();
        ctx.arc(curX, curY, 2, 0, Math.PI * 2);
        ctx.fill();
        ctx.shadowBlur = 0;
    }
}

function init() {
    particles = [];
    routes = [];
    for (let i = 0; i < 150; i++) {
        particles.push(new Particle());
    }
    for (let i = 0; i < 20; i++) {
        routes.push(new Route());
    }
}

function animate() {
    ctx.clearRect(0, 0, canvas.width, canvas.height);
    
    // Draw connections between close particles
    ctx.strokeStyle = `rgba(0, 240, 255, 0.03)`;
    for (let i = 0; i < particles.length; i++) {
        for (let j = i; j < particles.length; j++) {
            const dx = particles[i].x - particles[j].x;
            const dy = particles[i].y - particles[j].y;
            const dist = Math.sqrt(dx * dx + dy * dy);
            
            if (dist < 100) {
                ctx.beginPath();
                ctx.moveTo(particles[i].x, particles[i].y);
                ctx.lineTo(particles[j].x, particles[j].y);
                ctx.stroke();
            }
        }
    }

    particles.forEach(p => {
        p.update();
        p.draw();
    });

    routes.forEach(r => {
        r.update();
        r.draw();
    });

    requestAnimationFrame(animate);
}

init();
animate();

// Smooth scrolling for anchor links
document.querySelectorAll('a[href^="#"]').forEach(anchor => {
    anchor.addEventListener('click', function (e) {
        e.preventDefault();
        const targetId = this.getAttribute('href');
        if (targetId === '#') return;
        const targetElement = document.querySelector(targetId);
        if (targetElement) {
            targetElement.scrollIntoView({
                behavior: 'smooth'
            });
        }
    });
});

// Bar chart animation trigger on scroll
const observer = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
        if (entry.isIntersecting) {
            const bars = entry.target.querySelectorAll('.bar');
            bars.forEach((bar, index) => {
                const targetHeight = bar.getAttribute('data-height');
                bar.style.height = '0';
                setTimeout(() => {
                    bar.style.height = targetHeight;
                }, index * 100 + 100);
            });
            observer.unobserve(entry.target);
        }
    });
}, { threshold: 0.5 });

const graphMock = document.querySelector('.data-graph-mock');
if (graphMock) {
    observer.observe(graphMock);
}
